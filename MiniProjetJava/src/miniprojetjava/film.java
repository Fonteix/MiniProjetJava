package miniprojetjava;

public class film {
    
    public String nom;
    public boolean dispo;
    public String acteur;
    public int annee;
    public String realisateur;
    public String support;
    
    public film (String nom, boolean dispo, String acteur, int annee, String realisateur, String support) {
        this.nom = nom;
        this.dispo = dispo;
        this.acteur = acteur;
        this.annee = annee;
        this.realisateur = realisateur;
        this.support = support;   
    
    }
}
